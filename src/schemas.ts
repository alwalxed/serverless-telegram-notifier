import z from 'zod';

// ------------------------------------------------------------>
// -------------------------------------------------------------->
// ---------------------------------------------------------------->

// GET: /ping QUERY
export const pingSchema = z.object({
  key: z.string().min(8, "Bot bypass key too short").max(32, "Bot bypass key too long")
});

// ------------------------------------------------------------>
// -------------------------------------------------------------->
// ---------------------------------------------------------------->

// POST: /send JSON
export const notificationJSONSchema = z.object({
  message: z.string().min(1, "Message cannot be empty").max(50000, "Message too long"),
});

// POST: /send HEADER;
export const notificationHeaderSchema = z.object({
  Authorization: z.string().min(22, "Authorization key too short").max(44, "Authorization key too long")
});

